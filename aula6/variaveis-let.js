// Não podemos criar variaveis com palavras reservadas
// Variaveis precisam ter nomes significativos
// Não pode começar o nome de uma variavel com um número
// Não podem conter espaços ou traços
// Utilizamos camelCase (primeira palavra minuscula, demais palavras com a primeira letra maiuscula)
// Case-sensitive (letras maiusculas e minusculas fazem diferença)
// Não podemos redeclarar variaveis com let
// Não utilize var, utilize let

// Qual a diferença entre var, let e const?
// var -> é global e local e só tem escopo de função (hoisting)
// let -> é global, local e de bloco e não tem hoisting (não pode ser redeclarada)
// const -> é global, local e de bloco e não tem hoisting (não pode ser redeclarada e não pode ter seu valor reatribuido)