// Tipos de operadores: aritméticos, de atribuição, de comparação, lógicos e unários
// Exemplo de operadores aritméticos
const soma = 1 + 2; // Adição
const subtracao = 3 - 1; // Subtração
const multiplicacao = 2 * 3; // Multiplicação
const divisao = 6 / 2; // Divisão
const resto = 7 % 2; // Resto da divisão
const incremento = 1; // Incremento
const decremento = 2; // Decremento
const potenciacao = 2 ** 3; // Potenciação
const radiciacao = 9 ** 0.5; // Radiciação

// Exemplo de operadores de atribuição
let x = 1; // Atribuição
x += 2; // Adição
x -= 1; // Subtração
x *= 3; // Multiplicação
x /= 2; // Divisão
x %= 4; // Resto da divisão

// Exemplo de operadores de incremento e decremento
let y = 1; // Atribuição
y++; // Incremento
y--; // Decremento

// Exemplo de operadores de comparação
const igual = 1 === 1; // Igualdade de valor e tipo
const diferente = 1 !== 2; // Diferença de valor e tipo
const maior = 3 > 2; // Maior que
const menor = 1 < 2; // Menor que
const maiorIgual = 2 >= 2; // Maior ou igual a
const menorIgual = 1 <= 2; // Menor ou igual a

// Exemplo de operadores lógicos
const and = true && true; // E lógico
const or = true || false; // Ou lógico
const not = !true; // Negação lógica

// Exemplo de operadores unários
const negativo = -1; // Negação
const positivo = +1; // Sinal positivo
const string = "1"; // Conversão para número

// Coverter string para número
const inteiro = parseInt("1"); // Converte para inteiro
const flutuante = parseFloat("1.5"); // Converte para flutuante
const numero = Number("1"); // Converte para número

