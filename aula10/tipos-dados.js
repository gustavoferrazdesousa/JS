// Tipos de dados primitivos: string, number, undefined, null, boolean, symbol
// Exemplo de string
const nome = "GitHub Copilot"; // String é utilizado para texto

// Exemplo de number
const idade = 25; // Number é utilizado para números

// Exemplo de undefined
let endereco; // Undefined é utilizado para variáveis que não possuem valor

// Exemplo de null
const telefone = null; // Null é utilizado para variáveis que possuem valor nulo

// Exemplo de boolean
const isOnline = true; // Boolean é utilizado para valores verdadeiro ou falso

// Exemplo de symbol
const id = Symbol("id"); // Symbol é utilizado para valores únicos

// Tipos de dados por referência: array, object, function
// Exemplo de array
const numeros = [1, 2, 3, 4, 5]; // Array é utilizado para agrupar valores

// Exemplo de object
const pessoa = {
  nome: "João",
  idade: 30,
  endereco: "Rua A, 123",
}; // Object é utilizado para agrupar valores em pares chave-valor

// Exemplo de function
function somar(a, b) {
  return a + b;
} // Function é utilizado para agrupar instruções

// Exemplo de typeof
console.log(typeof nome); // Retorna o tipo de dado da variável